package com.example.android.qualityjatim;

public class DataTipe {
    private String id;
    private String rumah;
    private String luas;
    private String fasilitas;
    private String harga;
    private String keterangan;

    private String thubnail;


    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }


    public String getRumah() {
        return rumah;
    }
    public void setRumah(String rumah) {
        this.rumah = rumah;
    }


    public String getLuas() {
        return luas;
    }
    public void setLuas(String luas) {
        this.luas = luas;
    }


    public String getFasilitas() {
        return fasilitas;
    }
    public void setFasilitas(String fasilitas) {
        this.fasilitas = fasilitas;
    }


    public String getHarga() {
        return harga;
    }
    public void setHarga(String harga) {
        this.harga = harga;
    }


    public String getKeterangan() {
        return keterangan;
    }
    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }


    public String getThubnail() {
        return thubnail;
    }
    public void setThubnail(String thubnail) {
        this.thubnail = thubnail;
    }
}
